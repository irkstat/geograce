            ТТ&ndash;5, ТТП, ТН, ОТШ и Т5. Действие дальномера ДНТ&ndash;2 основано на получении в поле зрения трубы
            изображений двух половин как бы разрезанной горизонтальной плоскостью горизонтальной рейки, которые можно
            смещать одну относительно другой и путем последовательного совмещения изображений половин марок рейки
            измерять дальномерный угол. В комплект ДНТ&ndash;2 входят: насадка на объектив зрительной трубы 5 (рис. 2),
            состоящая из линзового компенсатора 7, дающего два наложенных друг на друга изображения рейки, и шкалового
            микроскопа 11 для отсчета по дальномерной шкале 9, связанной с перемещением полулинз компенсатора; бипризма
            3, устанавливаемая вместо сетки нитей; она имеет нарезанную на грань свою сетку нитей и делит изображение
            рейки на две части; щелевая диафрагма 1, навинчивающаяся на окулярный конец трубы; при горизонтальном
            положении щели она устраняет верхнюю часть одного и нижнюю часть другого изображения, что позволяет
            совмещать изображения половин марок рейки (на рис. 2 также показаны: 6 и 2 &ndash; объектив и окуляр
            зрительной трубы; 4 &ndash; фокусирующая линза; 10 и 13 &ndash; объектив и окуляр микроскопа; 12 &ndash;
            пластинка со шкалой микроскопа, 8 &ndash; защитные стекла); противовес, надеваемый на окулярную часть трубы
            для ее уравновешивания; две горизонтальные рейки, имеющие каждая по две пары марок; пара концевых марок
