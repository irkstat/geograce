        <h1 class="h1_description_general">Дальномеры оптические</h1>
        <p class="txt_description_general"><strong>Дальномеры оптические</strong>. Дальномерное измерение расстояний в
            геометрическом отношении является определением высоты S равнобедренного треугольника aAb (рис. 1) по
            формуле:</p>
        <p class="formula_description">$$S = {p'' \over &phi;''}l$$</p>
        <p class="txt_description_general">где:</p>
        <p class="txt_description_general">&phi;" = &ang;aAb &ndash; малый по величине дальномерный угол, называемый еще
            параллактическим;</p>
        <p class="txt_description_general">l = ab &ndash; базис;</p>

        <p class="txt_description_general">p" = 206264,8".</p>
        <br>
        <p class="txt_description_general">Одна из величин, &phi; или l, обычно является постоянной, а другая &ndash;
            переменной
            (измеряемой). По этому признаку дальномеры оптические подразделяются на дальномер с постоянным углом и
            дальномер с постоянным базисом. К тахеометрах-автоматах распространен третий тип дальномеров с переменным
            углом и базисом, имеющих редукционные устройства, позволяющие автоматически определять горизонтальные
            проложения наклонных дальностей. В конструктивном отношении дальномеры оптические весьма разнообразны, и по
            основам конструкций их можно подразделить на нитяные и двойного изображения.</p>
        <figure>
            <img class="img_description_general" style="max-width: 400px;"
