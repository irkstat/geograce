            <figcaption class="figcaption_general">Рис. 1 Принцип измерениярасстояния оптическим дальномером
            </figcaption>
        </figure>

        <p class="txt_description_general"><strong>Нитяными дальномерами</strong> могут служить горизонтальные
            дальномерные штрихи сетки зрительных труб некоторых теодолитов (точных и технических теодолитов), а также
            нивелиров и кипрегелей; постоянный дальномерный угол &phi;, называемый еще в данном случае
            диастимометрическим (измеряющим), равен угловому расстоянию между этими штрихами, а переменный базис l
            определяется отсчетами по рейке, имеющей обычно сантиметровые деления. Расстояние S находят по формуле:</p>
        <p class="formula_description">$$S=kl+c$$</p>

        <p class="txt_description_general">где:</p>
        <p class="txt_description_general">k = p'/&phi;' &ndash; коэффициент дальномера, обычно равный 100 (угол &phi; =
            34,38"), а c &ndash; постоянное слагаемое дальномера.</p>
        <br>
        <p class="txt_description_general">В зрительных трубах с внешней фокусировкой c = f + &delta;, где f &ndash;
            фокусное расстояние объектива, а &delta; расстояние от объектива до вертикальной оси теодолита; практически
            в этих трубах величину с принимают равной полуторной длине трубы от объектива до сетки нитей, если труба
            отфокусирована по удаленному предмету. Коэффициент к может быть получен по формуле:</p>
        <p class="formula_description">$$k = {(S-c)м \over lм}$$</p>

        <p class="txt_description_general">где S &ndash; тщательно измеренное лентой расстояние от вертикальной оси
            теодолита до рейки.</p>
        <p class="txt_description_general">В зрительных трубах с внутренней фокусировкой величины к и с изменяются с
            изменением расстояния, поэтому расстояния вычисляются по формуле:</p>
        <p class="formula_description">$$S_i=100l_i+q_i$$</p>
        <p class="txt_description_general">где l<sub>i</sub> &ndash; отсчитанная длина части рейки; q<sub>i</sub>
            &ndash; поправка, заранее определяемая для различных расстояний (обычно через каждые 10 м) и вычисляемая по
            формуле:</p>
        <p class="formula_description">$$q_i=S_i-100l_i$$</p>
        <p class="txt_description_general">здесь S<sub>i</sub> &ndash; тщательно измеряемое расстояние от вертикальной
            оси прибора до рейки.</p>
        <p class="txt_description_general">Для наклонных линий формула нитяного дальномера с вертикальной рейкой имеет
            вид:</p>
        <p class="formula_description">$$S=kl*cos^2 &alpha;+c*cos&alpha;$$</p>

        <p class="txt_description_general">где S &mdash; измеренное и приведенное к горизонту прибора расстояние;
            &alpha; &ndash; угол наклона зрительной трубы. Поправка для приведения наклонного расстояния к горизонту
            прибора обычно выбирается готовой из таблиц. </p>
        <br>
        <p class="txt_description_general">Точность определения расстояния нитяным дальномером порядка
            1:300&ndash;1:400. Более точно, чем нитяным дальномером, расстояния можно определять с помощью точного
            теодолита, например Т2 или ТБ&ndash;1, которым измеряется параллактический угол &phi;, а базисом l служит
            расстояние между двумя марками на горизонтальной рейке. Измеренное расстояние S вычисляют по формуле:</p>
        <p class="formula_description">$$S = {k \over &phi;''}$$</p>
        <p class="txt_description_general">где: k = lp"</p>
        <br>
        <p class="txt_description_general">Коэффициент k многократно определяют по расстояниям, измеренным мерной лентой
            (k = S<sub>i</sub>&phi;<sub>i</sub>") причем угол &phi; измеряют на тех же делениях лимба, на которых будут
            производиться измерения при определениях расстояний. Средняя квадратическая ошибка m<sub>S</sub> определения
            расстояния может быть подсчитана по формуле:</p>
        <p class="formula_description">$${m_S \over S}={S \over lp''}m_&phi;''$$</p>
        <p class="txt_description_general">С рейкой длиной 1&ndash;2 м расстояния в 400&ndash;500 м могут измеряться
            таким дальномером с точностью порядка 1:500. Приведение к горизонту наклонных расстояний производится так
            же, как при измерениях мерной лентой (см. Мерные линейные приборы). При необходимости определять
            значительные расстояния с большей точностью базисные марки устанавливаются на штативах с расстоянием между
            марками, равным, например, длине инварной проволоки или ленты. В этом случае, если базис l расположен
            симметрично и перпендикулярно к измеряемой линии, искомое расстояние вычисляется по формуле:</p>
        <p class="formula_description">$$S={l \over 2}*ctg{&phi; \over 2}$$</p>

        <p class="txt_description_general"><strong>Отечественные дальномеры двойного изображения</strong> имеются
            различных конструкций, которые можно разделить на две основные группы: дальномеры с постоянным базисом и
            дальномеры с постоянным углом. Дальномеры двойного изображения первой группы могут быть представлены,
            например, дальномерами ДНТ, ДНТ&ndash;2 (ДН&ndash;08, ДН&ndash;8), а второй группы &ndash; дальномерами
            ДД&ndash;3, (ДН&ndash;04), ДД&ndash;5 (ДН&ndash;10), ДНР&ndash;06.</p>
        <br>
        <p class="txt_description_general"><strong>Дальномеры ДНТ и ДНТ&ndash;2</strong> являются насадками на
            зрительную трубу теодолита; они одинаковы по своей конструкции и различаются тем, что насадка ДН
